-- Fix security issues for new functions by setting search_path
CREATE OR REPLACE FUNCTION public.update_gw_communications_updated_at()
RETURNS TRIGGER
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = ''
AS $$
BEGIN
    NEW.updated_at = now();
    RETURN NEW;
END;
$$;

